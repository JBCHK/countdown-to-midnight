def countdown_with_sleep(counter)
counter = 10
while counter >= 1
    puts "#{counter} SECOND(S)!"
    sleep 1
    counter -= 1
end 
    return "HAPPY NEW YEAR!"
end



def countdown(counter)
counter = 10
while counter >= 1
    puts "#{counter} SECOND(S)!"
    counter -= 1
end
    return "HAPPY NEW YEAR!"
end


